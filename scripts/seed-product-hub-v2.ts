// ===== MAIN =====
async function main() {
  console.log(`Seeding ${PRODUCTS.length} QBIT products…\n`);

  // Clear all existing products first
  await db.productRelation.deleteMany();
  await db.productOS.deleteMany();
  await db.productMedia.deleteMany();
  await db.productSpecification.deleteMany();
  await db.productFeature.deleteMany();
  await db.qbitProduct.deleteMany();
  console.log("  ✓ Cleared existing products\n");

  for (const p of PRODUCTS) {
    const product = await db.qbitProduct.create({
      data: {
        name: p.name,
        brand: "QBIT",
        model: p.model,
        slug: p.slug,
        deviceType: p.deviceType,
        category: p.category,
        description: p.desc,
        longDescription: p.longDesc,
        highlights: JSON.stringify(p.highlights),
        specifications: JSON.stringify(p.specs),
        features: JSON.stringify(p.features),
        isFeatured: p.feat ?? false,
        isTrending: p.trend ?? false,
        isBestSeller: p.best ?? false,
        isNewArrival: p.newArr ?? false,
        badgeLabel: p.badge,
        status: p.status,
        aiDiagnosticsSupported: p.aiDiag,
        drQbitSupported: p.drQbit,
        seoTitle: p.seoT,
        seoDescription: p.seoD,
        seoKeywords: p.seoK.join(","),
        tags: p.tags.join(","),
        qrCodeUrl: `https://hub.qbit.com/products/${p.slug}`,
        warrantyDuration: p.warranty,
        lastUpdated: new Date(),
        isActive: true,
      },
    });

    if (p.specs.length > 0) {
      await db.productSpecification.createMany({
        data: p.specs.map((s, i) => ({
          productId: product.id,
          property: s.property,
          value: s.value,
          group: s.group ?? null,
          sortIndex: i,
        })),
      });
    }

    if (p.features.length > 0) {
      await db.productFeature.createMany({
        data: p.features.map((f, i) => ({
          productId: product.id,
          icon: f.icon,
          title: f.title,
          description: f.description,
          sortIndex: i,
        })),
      });
    }

    console.log(`  ✓ ${p.slug} — ${p.name} (${p.specs.length} specs, ${p.features.length} features)`);
  }

  // Wire up related products
  const all = await db.qbitProduct.findMany({ select: { id: true, slug: true, category: true } });
  const bySlug = Object.fromEntries(all.map((p) => [p.slug, p]));

  // Link products in the same category as related (max 3 per product)
  const byCategory: Record<string, typeof all> = {};
  for (const p of all) {
    if (!byCategory[p.category ?? ""]) byCategory[p.category ?? ""] = [];
    byCategory[p.category ?? ""].push(p);
  }

  for (const p of all) {
    const siblings = (byCategory[p.category ?? ""] ?? []).filter((s) => s.id !== p.id).slice(0, 3);
    for (let i = 0; i < siblings.length; i++) {
      await db.productRelation.create({ data: { productId: p.id, relatedId: siblings[i].id, sortIndex: i } }).catch(() => {});
    }
  }

  const total = await db.qbitProduct.count();
  console.log(`\n=== Done. Total products: ${total} ===`);
}

main()
  .catch((e) => { console.error(e); process.exit(1); })
  .finally(() => db.$disconnect());
